                		<h1 class="title-quiz">Warburg Pincus</h1>
<p align="justify">                    		Warburg Pincus (WP), best known in India for its blockbuster $2 Billion exit from mobile services firm Bharti Airtel (in 2004-5), was among the first large global Private Equity investors to enter India. The firm has been investing in the country since the 1990s and witnessed multiple cycles since.
							<br/><br/>In the decade post the 2000-01 dotcom boom-and-bust, WP primarily preferred to invest in mature, well-established businesses (especially in Outsourcing, Manufacturing, Healthcare, Media, Banking and Shipping & Logistics). 2006 and 2010 witnessed its previous public faces in India - Pulak Prasad and Rajesh Khanna - leave to start their own funds.

<br/><br/>How has the firm being faring since and what's changed?
                    	</p>
              		</section>

