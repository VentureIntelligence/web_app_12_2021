                    	<p><b>First, The Official Line </b> (Source: <a target="_blank" href="http://www.WarburgPincus.com">Warburg Pincus Website</a>)
               </p>
                   <br>
<p align="justify">Founded in 1966, Warburg Pincus is a global private equity firm (its sole line of business) and has invested more than $50 billion in more than 720 companies in more than 35 countries around the world. The firm has more than $30 billion in assets under management. Its active portfolio of more than 125 companies is highly diversified by stage, sector and geography. Warburg Pincus is a growth investor and an experienced partner to management teams seeking to build durable companies with sustainable value. The firm is headquartered in New York with offices in Amsterdam, Beijing, Frankfurt, Hong Kong, London, Luxembourg, Mauritius, Mumbai, San Francisco, Sao Paulo and Shanghai.
</p>
 <br>
	<p>                		<b>What the India Data - from <a target="_blank" href="<?php echo GLOBAL_BASE_URL; ?>pelogin.php">Venture Intelligence</a> - Says:</b></p>
                   <br>
<p align="justify">Warburg Pincus has made over $3 Billion across 45 PE/VC investments in India since 1998. It has exited 26 companies returning over $4.5 Billion to investors.
</p>
<br>
<img width=790 style="border:1px solid black" src = "<?php echo GLOBAL_BASE_URL; ?>images/wp-images/overall.jpeg">

<br/><br/>
<p align="justify">In 2012, under a new leadership team, WP made a turn towards consumption led business in Financial Services (acquiring a majority stake in listed non-banking financial company Capital First and a minority one in regional small businesses focused lender AU Financiers).  The same year saw WP - which had invested in one of the first Internet portals in India, rediff.com, in 1999 - return its attention to the sector by leading a $32 million round for online classifieds firm Quikr. It has since made other bets including used car marketplace Cartrade, e-commerce focused courier firm Ecom Express and retail software maker Capillary Technologies.</p>
<br>
<p align="justify">

A consistent feature of WP investments is that when the firm makes an investment, it typically writes out a large cheque and also displays very high conviction: 80% of its investments, Venture Intelligence data shows, have been made as a sole investor. A classic example of this is the INR 1,200 Cr ($200 million) cheque - its largest ever - WP wrote in October 2014 to become the sole and first external institutional investor in Thrissur, Kerala headquartered jewelry retail chain Kalyan Jewelers. In those deals that it does share, the other investors are typically existing backers of the company who let WP lead the new investment round (i.e., contribute the largest share).
</p>
