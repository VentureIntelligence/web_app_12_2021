	<p>                		<b>Exits</b></p>
                   <br>
                    	<p align="justify"> As among the oldest PE investors in India, Warburg Pincus has seen its share of both spectacularly successful exits and well as write offs. Apart from the <a target="_blank" href="http://ventureintelligence.blogspot.in/2005/10/warburg-pincus-bharti-saga-ends-nets.html">much celebrated Bharti Airtel exit -  the largest in India till date </a> (in which WP netted a 6.6x return on its $292 million investment over 5 years), other good exits include Kotak Mahindra Bank ($700 M, 4.8X) and offroad tyre maker Alliance Tire Group ($404 M, 4X). Some of the loss maskers for WP have included jewelry exporter Vaibhav Gems, EPC firm Punj Loyd and CDs-turned-solar-panels maker Moser Baer.
</p>
