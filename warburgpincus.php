	<p>                		<b>Listed Vs. Unlisted</b></p>
                   <br>
<p align="justify"> In the boom years of 2004-2007, WP preferred to invest with publicly listed companies and went largely quiet in 2008 and 2009. Since 2008, Warburg Pincus has focused more on Private companies than listed companies.  Since 2010 - barring the $145 million Management Buy Out deal in publicly listed Capital First (formerly Future Capital) - WP has focused most of its attention in the unlisted segment.
</p>
<br>
